
public class ChessBoard {
	public void draw(int gridSize){
		String black = "B";
		String white = "W";
		
		for(int i=0;i<gridSize;i++){
			
			for(int j=0;j<gridSize;j++){
				if((j+i) %2 == 0){
					System.out.print(white + " ");
				}
				else if((j+i) %2 == 1){
					System.out.print(black + " ");
				}
			}
			System.out.println("");
		}
	}
}
